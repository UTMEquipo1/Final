                pps.setString(5,telefono.getText());
                pps.setString(6,direccion.getText());
                pps.setString(7,email.getText());
                pps.setString(8,tiponegocio.getText());
                pps.setString(9,nombrenegocio.getText());
                pps.setBinaryStream(10,fi);

                pps.executeUpdate();
                sql = "INSERT INTO Inventario VALUES(?,?,?)";
