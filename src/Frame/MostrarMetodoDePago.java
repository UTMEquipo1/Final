    private void MostrarTarjeta(ArrayList <Tarjeta> lista) {
         //To change body of generated methods, choose Tools | Templates.
        DefaultTableModel tablita = new DefaultTableModel();
        tablita.addColumn("Nombre");
        tablita.addColumn("#Tarjeta");
        tablita.addColumn("Vencimiento");
        tablita.addColumn("CVV");
        tablaTarjeta.setModel(tablita);
        
        Object datos[]= new Object[4];
        for(int i=0;i<lista.size();i++){
            datos[0]=lista.get(i).getNombre();
            datos[1]=lista.get(i).getNum_tarjeta();
            datos[2]=lista.get(i).getFecha_vencimiento();
            datos[3]=lista.get(i).getNumeros_seguridad();
            tablita.addRow(datos);
        }
           
        tablaTarjeta.setModel(tablita);
        tablaTarjeta.setRowHeight(64);
       
    }
